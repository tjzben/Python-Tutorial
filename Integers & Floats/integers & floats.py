# Arithmetic Operators
x, y = 3 , 2

# Addition
print(x + y) # = 5
# Substraction
print(x - y) # = 1
# Multiplication
print(x * y) # = 6
# Division
print(x / y) # = 1.5
# Integer division - no remainder
print(x // y) # = 1
# Modulus - remainder of the divison
print(x % y) # = 1s
# Negative value assignment
print(-x) # = -3
# Absolute number
print(abs(-x)) # = 3
# Cast integer value
print(int( 3.9 )) # = 3
# Cast floating value
print(float( 3 )) # = 3.0
# Exponential 
print(x ** y) # = 9

# Comparison Operators - returns True or False accordingly to the condition

a, b = 10, 20
# Equal to
print(a == b)
# Not equal to 
print(a != b)
# Greater than
print(a > b)
# Greater or equal to
print(a >= b)
# Less than
print(a < b)
# Less than or equal to
print( a <= b)

